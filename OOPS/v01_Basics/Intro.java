package OOPS.v01_Basics;

/**
 * 📘 TOPIC: INTRODUCTION TO OOPs
 * 
 * 1. Procedural Programming: Logic follows a linear sequence of instructions. 
 *    Data and functions are separate.
 * 2. Object-Oriented Programming (OOPs): Programs are organized around "Objects"
 *    that contain both data (fields) and behavior (methods).
 */

public class Intro {
    public static void main(String[] args) {
        System.out.println("--- Procedural vs OOPs ---");
        
        // [1] BASIC: Thinking in Objects
        // Instead of separate variables like 'studentName' and 'studentRoll',
        // we create a 'Student' object that encapsulates both.
        
        System.out.println("OOPs Core Pillars:");
        System.out.println("1. Encapsulation: Data hiding/security.");
        System.out.println("2. Inheritance: Code reusability.");
        System.out.println("3. Polymorphism: Many forms of behavior.");
        System.out.println("4. Abstraction: Simplification/Implementation hiding.");

        // [2] INTERMEDIATE: Why OOPs?
        // - Easier to maintain large codebases.
        // - Dry (Don't Repeat Yourself) principle through Inheritance.
        // - Modular design for Parallel development.
    }
}
